// Print the number is + , - or 0
function posOrNeg(num){
    if(num===0){
        console.log(num,"is Zero")
    }
    else if(num>0){
        console.log(num,"is a Positive Number")
    }
    else{
        console.log(num,"is a Negative Number")
    }
}
posOrNeg(0)
posOrNeg(5)
posOrNeg(-50)
posOrNeg(6)
posOrNeg(1)
posOrNeg(-59)
posOrNeg(-5554)
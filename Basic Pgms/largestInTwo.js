// Take 2 numbers as input and print the largest number.
function largeInTwo(num1,num2) {
    if(num1>num2){
        console.log("The Largest Number between "+num1+" and "+num2+" is "+num1)
    }else{
        console.log("The Largest Number between "+num1+" and "+num2+" is "+num2)
    }
}
largeInTwo(12,56)
largeInTwo(12,-6)
// Take 2 numbers as input and print the smallest number.
function smallInTwo(num1,num2) {
    if(num1<num2){
        console.log("The Smallest Number between "+num1+" and "+num2+" is "+num1)
    }else{
        console.log("The smallest Number between "+num1+" and "+num2+" is "+num2)
    }
}
smallInTwo(12,56)
smallInTwo(12,-6)
